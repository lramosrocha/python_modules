# +	plus			# addition
# -	minus			# subtraction
# /	slash			# division
# *	asterisk		# multiplication
# %	percent			# modulo (shows remainder of division)
# <	less- than		# boolean, if left number is less-than the right one
# >	greater- than		# same as the one before, but if the left number is greater
# <=	less- than- equal	# boolean, if left number is less-than or equal to the right one
# >=	greater- than- equal	# same as the one before, but if the left number is greater or equal

